# ---------- 微小テール(改定後最終年の端数繰越)が無いこと ----------
#  改定(均等)償却の最終年は備忘1円まで一括計上され、翌年に数円の端数が繰り越されない。
#  「最終年の償却費が直前年の1/100未満」を微小テールとみなし、全マトリクスで不存在を検査。
tails = []
for n in sorted(RATES):
    r, rev, g = RATES[n]
    for label, d in STARTS.items():
        for cost in COSTS:
            deps = [row[1] for row in fetch(cost, d, n, r, g, rev)]
            if len(deps) >= 2 and deps[-1] * 100 < deps[-2]:
                tails.append(f"{n}年{label}/cost{cost}: 直前={deps[-2]} 最終={deps[-1]}")
chk.eq("全マトリクスで微小テール(数円の端数繰越)が無い", tails, [])

# ---------- 期首取得は耐用年数ちょうどで償却完了する ----------
#  200%定率法の設計上、期首(初年度12か月)取得は耐用年数 n 年ちょうどで備忘1円に到達する。
#  以前は 12年・17年(改定償却率×残存年数=1.0)が n+1 年になっていた回帰の固定。
period_bad = []
for n in sorted(RATES):
    r, rev, g = RATES[n]
    for cost in COSTS:
        rows = fetch(cost, "2024-01-01", n, r, g, rev)
        if len(rows) != n:
            period_bad.append(f"{n}年期首/cost{cost}: {len(rows)}行(期待{n})")
chk.eq("期首取得は耐用年数ちょうどで完了(12年・17年の端数繰越も解消)", period_bad, [])

